using System;
using System.Collections.Generic;
using System.Text;

namespace Task4
{
    class Model
    {
        public int Plus(int a, int b)
        {
            return a + b;
        }

        public int Minus(int a, int b)
        {
            return a - b;
        }

        public int Mult(int a, int b)
        {
            return a * b;
        }

        public double Div(int a, int b)
        {
            return (double)a / (double)b;
        }
    }
}
